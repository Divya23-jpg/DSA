
#include<iostream>
using namespace std;
int main()
{
    int n;
    cout<<"Enter Number::";
    cin>>n;

    for(int i=1;i<=n;i++){
     //star
        for(int j=1;j<=2*i-2;j++){
             cout<<"  ";
 
        }
        //space
        for(int j=1;j<=n-i+1;j++){
             cout<<"* ";
 
        }
      

        cout<<endl;
    }
    for(int i=n-1;i>=1;i--){
     //star
        for(int j=1;j<=2*i-2;j++){
             cout<<"  ";
 
        }
        //space
        for(int j=1;j<=n-i+1;j++){
             cout<<"* ";
 
        }
      

        cout<<endl;
    }
    return 0;
}